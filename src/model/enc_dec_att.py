from torch import nn
from torchvision import models
import torch
import math
import torch.nn.functional as F

class encoder(nn.Module):
  """
  my encoder uses the simple cnn based pretrained resnet 50 for 
  getting the image information and projects to the embedding space

  Attributes:
    enc_dim: Output embedding dimension
    resnet: Frozen ResNet50 backbone (without final FC layer)
    linear: Linear projection layer
    bn: Batch normalization layer
  """
  def __init__(self, enc_dim):
    """
    Initialize the encoder.
        
    Args:
      enc_dim: Dimension of the output embedding vector.

    Raises:
      ValueError: If enc_dim is not positive
    """
    super().__init__()

    if enc_dim<=0:
      raise ValueError(f"enc_dim must be positive, got {enc_dim}")

    self.resnet=models.resnet50(pretrained=True)
    in_features=self.resnet.fc.in_features

    for param in self.resnet.parameters():
      param.requires_grad_(False)

    modules=list(self.resnet.children())[:-2]
    self.resnet=nn.Sequential(*modules)

    self.linear=nn.Linear(in_features, enc_dim)
    self.bn=nn.BatchNorm1d(enc_dim, momentum=0.01)

  def forward(self, images):
    """
    Extract features from input images.

    Args:
      images: (batch_size, 3, 224, 224)

    Returns:
      features: (batch_size, embed_dim)

    Raises:
      ValueError: If input tensor has wrong dimensions
    """
    if images.dim() != 4:
      raise ValueError(
          f"Expected 4D tensor (B, C, H, W), got {images.dim()}D"
      )
    features=self.resnet(images)
    features=features.view(features.size(0), features.size(1), -1)
    features=features.permute(0, 2,1)
    b=features.size(0)
    n=features.size(1)
    features=self.linear(features)
    features=features.view(-1, features.size(-1))
    features=self.bn(features)
    features = features.view(b, n, -1)
    
    return features

class Attention(nn.Module):
  """
  Scaled dot-product attention mechanism.
    
  Computes attention weights over encoder outputs based on the
  current decoder hidden state, then produces a context vector
  as a weighted sum of encoder outputs.
    
  Implements: Attention(Q, K, V) = softmax(QK^T / sqrt(d)) * V
    
  Attributes:
      Wq: Query projection
      Wk: Key projection  
      Wv: Value projection
      dim: Attention dimension (for scaling)
  """
  def __init__(self, encoder_dim, decoder_dim, attention_dim):
    """
    Initialize attention mechanism.
        
    Args:
        encoder_dim: Dimension of encoder outputs
        decoder_dim: Dimension of decoder hidden state
        attention_dim: Internal attention dimension
    """
    super().__init__()
    self.Wq=nn.Linear(decoder_dim, attention_dim)
    self.Wk=nn.Linear(encoder_dim, attention_dim)
    self.Wv=nn.Linear(encoder_dim, attention_dim)

    self.dim=attention_dim

  def forward(self, enc, sprev):
    """
    Compute attention-weighted context vector.
        
    Args:
        encoder_out: Encoder outputs (batch, num_pixels, encoder_dim)
        decoder_hidden: Decoder hidden state (batch, decoder_dim)
        
    Returns:
        Tuple of:
            - context: Weighted context vector (batch, attention_dim)
            - weights: Attention weights (batch, num_pixels)
    """

    q=self.Wq(sprev)# (B, attention_dim)
    k=self.Wk(enc)# (B, N, attention_dim)
    v=self.Wv(enc)# (B, N, attention_dim)

    att=torch.bmm(q.unsqueeze(1), k.transpose(1,2))/math.sqrt(self.dim)# (B, 1, N)
    att=torch.softmax(att, dim=2)# (B, 1, N)

    context=torch.bmm(att, v).squeeze(1) # (B, attention_dim)
    return context, att.squeeze(1)

class decoder(nn.Module):
  """
  this decoder uses lstm based decoder for generating the captions from the image summary
  generated by the encoder.

  Attributes:
    embed_size: Word embedding dimension
    hidden_size: LSTM hidden state dimension
    attention_dim: The dimension of attention mechanism
    vocab_size: Size of the vocabulary
    embed: Word embedding layer
    lstm: LSTM recurrent layer
    linear: Output projection to vocabulary
  """
  def __init__(self,embed_size,hidden_size,vocab_size, encoder_dim, attention_dim, num_layers=1):
    """
    Initialize the decoder.

    Args:
      embed_size: Dimension of word embeddings
      hidden_size: Dimension of LSTM hidden states
      vocab_size: Number of words in vocabulary
      num_layers: Number of LSTM layers (default: 1)
    
    Raises:
      ValueError: If any dimension is not positive
    """
    super().__init__()

    if embed_size <= 0 or hidden_size <= 0 or vocab_size <= 0 or num_layers<=0 or attention_dim<=0:
      raise ValueError("All dimensions must be positive")
    
    self.embed=nn.Embedding(vocab_size,embed_size)
    self.attention=Attention(encoder_dim, hidden_size, num_layers, attention_dim)
    self.lstm=nn.LSTMCell(embed_size+attention_dim, hidden_size)
    self.linear=nn.Linear(hidden_size,vocab_size)

  def forward(self,features,captions):
    """
    Forward pass with teacher forcing.

    Args:
      features: (batch_size, embed_size) - encoded image features
      captions: (batch_size, max_len) - tokenized captions

    Returns:
      outputs: (batch_size, max_len, vocab_size) - predicted logits
    """
    captions=captions[:,:-1] # "<start> hello my name <end> " we drop the end
    embeddings=self.embed(captions)
    batch_size=features.size(0)

    h_state=torch.zeros(batch_size, self.lstm.hidden_size).to(features.device)
    c_state=torch.zeros(batch_size, self.lstm.hidden_size).to(features.device)

    outputs=[]

    for i in range(captions.size(1)):
        context, _=self.attention(features, h_state)
        lstm_input=torch.cat((embeddings[:, i, :], context), dim=1)
        h_state, c_state=self.lstm(lstm_input, (h_state, c_state))
        outputs.append(self.linear(h_state))

    return torch.stack(outputs, dim=1)
  
  def sample(self,features,max_len=20):
    """
    using greedy decoding to generate captions

    Args:
      features: Encoded image features (1, embed_size)
      max_len: Maximum caption length (default: 20)
        
    Returns:
      List of token IDs representing the generated caption
    """
    output_ids=[]
    batch_size=features.size(0)

    h_state=torch.zeros(batch_size, self.lstm.hidden_size).to(features.device)
    c_state=torch.zeros(batch_size, self.lstm.hidden_size).to(features.device)

    inputs=self.embed(torch.tensor([1]*batch_size).to(features.device))

    for i in range(max_len):
      context, _=self.attention(features, h_state)

      lstm_input=torch.cat((inputs, context), dim=1)
      h_state, c_state=self.lstm(lstm_input, (h_state, c_state))
      predicted=self.linear(h_state).argmax(1)
      output_ids.append(predicted.item())

      if predicted.item()==2:
        break

      inputs=self.embed(predicted)
    return output_ids
  def beam(self,features,k,max_len=20):
    """
    generates a caption using beam search decoding. maintains
    the top k most probable sequences at each step.

    Args:
      features: image summary produced by encoder(1, embed_dim)
      k: beam width
      max_len: the absolute max length generated by the model, if
              <end> token is not produced
    Returns:
      list of token ids of each word in the caption
    """
    vocab_size=self.linear.out_features
    start_token=1
    end_token=2
    device=features.device
    
    k_prev_words=torch.full((k,1),start_token,dtype=torch.long,device=device)
    top_k_scores=torch.zeros(k,1,device=device)
    
    features=features.expand(k,*features.shape[1:])
    inputs=self.embed(k_prev_words[:,-1])
    
    h_state=torch.zeros(k,self.lstm.hidden_size).to(device)
    c_state=torch.zeros(k,self.lstm.hidden_size).to(device)
    
    context,_=self.attention(features,h_state)
    lstm_input=torch.cat((inputs,context),dim=1)
    h_state,c_state=self.lstm(lstm_input,(h_state,c_state))
    output=self.linear(h_state)
    
    log_probs=F.log_softmax(output,dim=1) 
    
    top_k_scores,top_k_words=log_probs[0].topk(k,dim=0)
    
    k_prev_words=top_k_words.unsqueeze(1) 
    top_k_scores=top_k_scores.unsqueeze(1)
    
    for i in range(1,max_len):
        inputs=self.embed(k_prev_words[:,-1])
        
        context,_=self.attention(features,h_state)
        lstm_input=torch.cat((inputs,context),dim=1)
        
        h_state,c_state=self.lstm(lstm_input,(h_state,c_state))
        output=self.linear(h_state)
        log_probs=F.log_softmax(output,dim=1)

        total_scores=top_k_scores+log_probs 
        
        total_scores=total_scores.view(-1)
        top_k_scores,top_k_indices=total_scores.topk(k,dim=0)
        
        prev_word_indices=top_k_indices//vocab_size 
        next_word_ids=top_k_indices%vocab_size      
        
        new_sequences=torch.cat(
            [k_prev_words[prev_word_indices],next_word_ids.unsqueeze(1)],dim=1
        )
        k_prev_words=new_sequences
        
        h_state=h_state[prev_word_indices]
        c_state=c_state[prev_word_indices]
        
        top_k_scores=top_k_scores.unsqueeze(1)
        
        if next_word_ids[0].item()==end_token:
            break

    return k_prev_words[0].tolist()